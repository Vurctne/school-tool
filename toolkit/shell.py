class TkShell(ttk.Frame):
    """Top-level shell frame — pack into the root Tk window with fill=both, expand=True."""

    def __init__(
        self,
        root: tk.Tk,
        fonts: FontMap,
        tools: list[type[BaseTool]],
        log_path: Path | None = None,
        **kwargs: Any,
    ) -> None:
        super().__init__(root, **kwargs)

        self._root = root
        self._fonts = fonts
        self._log_path = log_path

        # Instantiate each tool class → instance
        # tools is a list[type[BaseTool]] from registry.all_tools()
        self._tools: list[BaseTool] = [cls() for cls in tools]
        self._tool_map: dict[str, BaseTool] = {t.id: t for t in self._tools}

        # Per-tool cached input values (preserved across tab switches)
        self._input_cache: dict[str, dict[str, Any]] = {t.id: {} for t in self._tools}

        # Active tool
        self._active_id: str = self._tools[0].id if self._tools else ""

        # User tab (built lazily on first activation)
        self._user_frame: UserFrame | None = None

        # Unlock CTA frame (built lazily on first gated activation)
        self._unlock_frame: tk.Frame | None = None
        self._unlock_title_var: tk.StringVar | None = None

        # Per-tool executor (one per tool instance so runs don't queue globally)
        self._executors: dict[str, ThreadPoolExecutor] = {
            t.id: ThreadPoolExecutor(max_workers=1) for t in self._tools
        }

        # Per-tool filter state (transient UI — not part of ToolResult)
        self._tool_filter: dict[str, str | None] = {}
        self._tool_table_specs: dict[str, TableSpec | None] = {}
        self._tool_rails: dict[str, SelectableList | None] = {}
        self._tool_filter_chips: dict[str, tk.Frame | None] = {}

        # Per-tool debounce after() IDs for RangeInput live-preview callbacks.
        # Maps tool_id -> {input_key -> after_id | None}
        self._range_debounce: dict[str, dict[str, str | None]] = {}

        # Root-window <Configure> debounce (Fix 2 — Round 13).
        # Fires once per ~50 ms idle after any window-resize drag, preventing
        # Tk's geometry manager from thrashing on every pixel.
        self._root_configure_after: str | None = None

        # Drag-state suspension (Fix 5 — Round 17).
        # While the user is actively dragging the window edge the root
        # <Configure> fires on every pixel.  We set _drag_active=True on the
        # first event and let all canvas Configure handlers skip their work
        # entirely.  _on_drag_settled() runs 150 ms after the last Configure
        # event and triggers a single clean layout pass, then clears the flag.
        # This is more aggressive than debouncing alone (which still schedules
        # an after() call on every pixel); suspension drops ALL intermediate
        # work during the drag.  The 150 ms settle window is longer than the
        # 100 ms debounce so it acts as the outer gate.
        self._drag_active: bool = False
        self._drag_settle_after: str | None = None

        # Per-tool pending canvas-configure callbacks that were skipped during
        # drag; we re-fire them once on settle.  Each entry is a 0-arg callable.
        self._pending_canvas_configures: list[Any] = []

        # Install global exception handler
        self._root.report_callback_exception = self._handle_tk_exception

        # Build layout
        self._build_layout()

        # Activate first tool
        if self._active_id:
            self._activate_tool(self._active_id)

