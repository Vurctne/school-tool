    def _build_input_widget(self, parent: tk.Widget, inp: InputSpec, tool: BaseTool) -> tk.Widget:
        """Dispatch an InputSpec → the matching primitive widget."""
        if isinstance(inp, FileInput):
            cached = self._input_cache[tool.id].get(inp.key)

            def _on_pick(p: Path, key: str = inp.key, tid: str = tool.id) -> None:
                self._input_cache[tid][key] = p

            return FileRow(
                parent,
                label=inp.label,
                on_pick=_on_pick,
                filetypes=inp.filetypes if inp.filetypes else [("All files", "*.*")],
                initial_path=cached if isinstance(cached, Path) else None,
            )

        if isinstance(inp, TextInput):
            cached = str(self._input_cache[tool.id].get(inp.key, ""))
            return TextField(
                parent,
                label=inp.label,
                value=cached,
                placeholder=inp.placeholder,
                max_length=inp.max_length,
            )

        if isinstance(inp, NumberInput):
            cached = str(self._input_cache[tool.id].get(inp.key, ""))
            return NumberField(
                parent,
                label=inp.label,
                value=cached,
                min_value=inp.min_value,
                max_value=inp.max_value,
                decimals=inp.decimals,
            )

        if isinstance(inp, CurrencyInput):
            cached = str(self._input_cache[tool.id].get(inp.key, ""))
            return CurrencyField(parent, label=inp.label, value=cached)

        if isinstance(inp, DateInput):
            return DateField(parent, label=inp.label, default=inp.default)

        if isinstance(inp, SecretInput):
            return SecretField(
                parent,
                label=inp.label,
                pattern=inp.pattern,
                remember_key=inp.remember_key,
            )

        if isinstance(inp, RangeInput):
            return self._build_range_widget(parent, inp, tool)

        # Fallback (should never happen with a well-typed InputSpec)
        return TextField(parent, label=getattr(inp, "label", str(inp)))

    def _build_range_widget(self, parent: tk.Widget, inp: RangeInput, tool: BaseTool) -> tk.Frame:
        """Build a labelled ttk.Scale (slider) widget for a RangeInput InputSpec.

        Layout: label row | [min%] slider [max%]  [ value ] %  hint-text

        When ``inp.numeric_box`` is False (default) the value display is a read-only
        Label (e.g. "101 %").  When True two separate DoubleVars are used:

        * ``actual_var`` — the user-facing value; accepts any value > 0 (no upper
          cap). This is what ``_input_cache`` stores and ``run()`` sees.
        * ``slider_var`` — drives the ttk.Scale knob, clamped to
          ``[min_value, max_value]``. When the user types a value outside the
          slider range the knob snaps to the nearest endpoint, giving a visual
          "at maximum" cue without capping the typed value.

        Entry validation (FocusOut / Return):
          * Non-numeric → revert to previous actual value.
          * <= 0 → revert to previous actual value.
          * Any value > 0 → accepted; actual_var updated; slider_var clamped.

        When ``inp.live`` is True, ``tool.preview_update(key, value)`` is called
        debounced at ~100 ms whenever ``actual_var`` changes.  The value passed
        is the **actual** (unclamped) value.

        Visual layout when ``numeric_box=True``:
            Over-budget threshold
              100%  ━━━━━●━━━━━━━━━━━━  120%      [ 101 ] %   Type any value > 0 …
        """
        tid = tool.id

        # Ensure debounce dict entry exists for this tool.
        if tid not in self._range_debounce:
            self._range_debounce[tid] = {}
        self._range_debounce[tid].setdefault(inp.key, None)

        # Seed input cache with the default value if nothing cached yet.
        cached = self._input_cache[tid].get(inp.key)
        if cached is None:
            self._input_cache[tid][inp.key] = inp.default
            initial = inp.default
        else:
            try:
                initial = float(cached)
            except (TypeError, ValueError):
                initial = inp.default

        container = tk.Frame(parent, bg=tokens.BG_MUTED)

        # Row 1: label
        # When numeric_box=True, the label is a live StringVar that appends the
        # current value (e.g. "Over-budget threshold: 101%") so the user always
        # sees the active value without having to look at the entry on the right.
        # When numeric_box=False, it is a plain static label.  (Fix 1 — Round 13)
        import tkinter.font as _tkfont  # noqa: PLC0415

        _label_var: tk.StringVar | None = None
        _label_font = _tkfont.Font(family="Segoe UI", size=tokens.FS_13)
        # Decimal count for percentage formatting: 0 for step >= 1, else 1.
        # Defined unconditionally so the _on_actual_change closure can capture it
        # without a potential-unbound-variable warning from mypy --strict.
        _step_decimals: int = 0 if inp.step >= 1.0 else 1
        if inp.numeric_box:
            _initial_label = f"{inp.label}: {initial:.{_step_decimals}f}%"
            _label_var = tk.StringVar(value=_initial_label)
            lbl = tk.Label(
                container,
                textvariable=_label_var,
                bg=tokens.BG_MUTED,
                fg=tokens.FG_1,
                font=_label_font,
                anchor="w",
            )
        else:
            lbl = tk.Label(
                container,
                text=inp.label,
                bg=tokens.BG_MUTED,
                fg=tokens.FG_1,
                font=_label_font,
                anchor="w",
            )
        lbl.pack(fill="x")

        # Row 2: [min%] slider [max%]  [ value ] %  hint
        row2 = tk.Frame(container, bg=tokens.BG_MUTED)
        row2.pack(fill="x", pady=(tokens.SP_1, 0))

        # Determine display format from step size.
        _fmt = ":.0f" if inp.step >= 1.0 else ":.1f"

        def _fmt_num(v: float) -> str:
            return format(v, _fmt[1:])

        def _fmt_val(v: float) -> str:
            return _fmt_num(v) + " %"

        def _clamp(v: float) -> float:
            return max(inp.min_value, min(inp.max_value, v))

        # Value display — label (default) or numeric entry (when numeric_box=True).
        _value_lbl: tk.Label | None = None
        _num_entry: tk.Entry | None = None
        _num_entry_var: tk.StringVar | None = None
        _actual_var: tk.DoubleVar | None = None

        if inp.numeric_box:
            # -----------------------------------------------------------------
            # Decoupled-var path (numeric_box=True)
            # actual_var: user-facing, any value > 0 (no upper cap)
            # slider_var: drives the Scale knob, always clamped to [min, max]
            # -----------------------------------------------------------------
            actual_var = tk.DoubleVar(value=initial)
            slider_var = tk.DoubleVar(value=_clamp(initial))
            _actual_var = actual_var

            # Slider → actual (slider always emits clamped values, so just forward)
            def _on_slider_change(*_args: object) -> None:
                v = slider_var.get()
                if inp.step and inp.step > 0:
                    v = round(v / inp.step) * inp.step
                current_actual = actual_var.get()
                if abs(v - current_actual) > 1e-9:
                    actual_var.set(v)

            slider_var.trace_add("write", _on_slider_change)

            # actual_var → update row-1 label (live value display — Fix 1 Round 13)
            #           → update entry display (if entry doesn't have focus)
            #           → update input cache
            #           → fire debounced preview
            def _on_actual_change(*_args: object) -> None:
                v = actual_var.get()
                # Update the row-1 label to show the current value next to the
                # label text (cheap — StringVar.set() does not trigger layout).
                if _label_var is not None:
                    try:
                        _label_var.set(f"{inp.label}: {v:.{_step_decimals}f}%")
                    except Exception:
                        pass
                # Update entry text only when the entry isn't being typed into
                try:
                    if (
                        _num_entry is not None
                        and _num_entry_var is not None
                        and _num_entry.focus_get() is not _num_entry
                    ):
                        _num_entry_var.set(_fmt_num(v))
                except Exception:
                    pass
                # Keep input cache in sync with the actual (unclamped) value
                self._input_cache[tid][inp.key] = v

                if not inp.live:
                    return

                # Debounce preview fire on actual_var value
                pending = self._range_debounce[tid].get(inp.key)
                if pending is not None:
                    try:
                        self._root.after_cancel(pending)
                    except Exception:
                        pass

                def _fire_preview(fire_v: float = v) -> None:
                    self._range_debounce[tid][inp.key] = None
                    result = tool.preview_update(inp.key, fire_v)
                    if result is not None:
                        self._render_result(tid, result)

                after_id = self._root.after(100, _fire_preview)
                self._range_debounce[tid][inp.key] = after_id

            actual_var.trace_add("write", _on_actual_change)

            # ---- range label left ----
            _range_lbl_min = tk.Label(
                row2,
                text=_fmt_num(inp.min_value) + "%",
                bg=tokens.BG_MUTED,
                fg=tokens.FG_3,
                font=_tkfont.Font(family=tokens.FONT_MONO_FALLBACK, size=tokens.FS_12),
            )
            _range_lbl_min.pack(side="left", padx=(0, tokens.SP_1))

            scale = ttk.Scale(
                row2,
                from_=inp.min_value,
                to=inp.max_value,
                orient="horizontal",
                variable=slider_var,
            )
            scale.pack(side="left", fill="x", expand=False, ipadx=0)
            # Cap slider width so it doesn't stretch the full row width.
            scale.configure(length=260)

            # ---- range label right ----
            _range_lbl_max = tk.Label(
                row2,
                text=_fmt_num(inp.max_value) + "%",
                bg=tokens.BG_MUTED,
                fg=tokens.FG_3,
                font=_tkfont.Font(family=tokens.FONT_MONO_FALLBACK, size=tokens.FS_12),
            )
            _range_lbl_max.pack(side="left", padx=(tokens.SP_1, tokens.SP_3))

            # ---- number entry ----
            _num_entry_var = tk.StringVar(value=_fmt_num(initial))
            _num_entry = tk.Entry(
                row2,
                textvariable=_num_entry_var,
                width=6,
                fg=tokens.FG_1,
                font=_tkfont.Font(family="Segoe UI", size=tokens.FS_12),
                relief="sunken",
                bd=1,
                justify="right",
            )
            _num_entry.pack(side="left", padx=(0, tokens.SP_1))

            # ---- % suffix label ----
            _pct_lbl = tk.Label(
                row2,
                text="%",
                bg=tokens.BG_MUTED,
                fg=tokens.FG_2,
                font=_tkfont.Font(family="Segoe UI", size=tokens.FS_12),
            )
            _pct_lbl.pack(side="left", padx=(0, tokens.SP_2))

            # ---- hint text ----
            _hint_min = _fmt_num(inp.min_value)
            _hint_max = _fmt_num(inp.max_value)
            _hint_lbl = tk.Label(
                row2,
                text=f"Type any value > 0 — drag limited to {_hint_min}–{_hint_max}%",
                bg=tokens.BG_MUTED,
                fg=tokens.FG_3,
                font=_tkfont.Font(family="Segoe UI", size=tokens.FS_12, slant="italic"),
            )
            _hint_lbl.pack(side="left", padx=(0, 0))

            # Track the last committed value so invalid input can be reverted.
            _last_committed: list[float] = [initial]

            def _commit_entry(*_args: object) -> None:
                """Validate and commit the typed value.

                Accepts any value > 0 — no upper cap.
                Rejects non-numeric input and values <= 0 (reverts to last good).
                Updates actual_var (which propagates to cache + preview).
                Updates slider_var to clamp(typed, min, max) for knob position.
                """
                assert _num_entry_var is not None  # narrowing for mypy
                text = _num_entry_var.get().strip()
                try:
                    parsed = float(text)
                except ValueError:
                    _num_entry_var.set(_fmt_num(_last_committed[0]))
                    return
                if parsed <= 0:
                    _num_entry_var.set(_fmt_num(_last_committed[0]))
                    return
                _last_committed[0] = parsed
                _num_entry_var.set(_fmt_num(parsed))
                # Update slider knob (clamped) without triggering actual_var feedback
                slider_var.set(_clamp(parsed))
                # Update actual_var to the unclamped typed value
                actual_var.set(parsed)

            _num_entry.bind("<FocusOut>", _commit_entry)
            _num_entry.bind("<Return>", _commit_entry)

            # Use slider_var as the Scale-facing var; expose actual_var as _scale_var
            # (the run() path reads _input_cache, not _scale_var directly,
            # but tests may inspect _scale_var — point it to actual_var so
            # tests that check "what value will run() see" read the right thing).
            _scale_var_exposed = actual_var

        else:
            # -----------------------------------------------------------------
            # Simple single-var path (numeric_box=False) — unchanged behaviour
            # -----------------------------------------------------------------
            scale_var = tk.DoubleVar(value=initial)
            _scale_var_exposed = scale_var

            def _on_scale_change(*_args: object) -> None:
                raw = scale_var.get()
                if inp.step and inp.step > 0:
                    snapped = round(raw / inp.step) * inp.step
                else:
                    snapped = raw
                _value_lbl.configure(text=_fmt_val(snapped))  # type: ignore[union-attr]
                self._input_cache[tid][inp.key] = snapped

                if not inp.live:
                    return

                pending = self._range_debounce[tid].get(inp.key)
                if pending is not None:
                    try:
                        self._root.after_cancel(pending)
                    except Exception:
                        pass

                def _fire_preview(v: float = snapped) -> None:
                    self._range_debounce[tid][inp.key] = None
                    result = tool.preview_update(inp.key, v)
                    if result is not None:
                        self._render_result(tid, result)

                after_id = self._root.after(100, _fire_preview)
                self._range_debounce[tid][inp.key] = after_id

            scale = ttk.Scale(
                row2,
                from_=inp.min_value,
                to=inp.max_value,
                orient="horizontal",
                variable=scale_var,
            )
            scale.pack(side="left", fill="x", expand=True)

            _value_lbl = tk.Label(
                row2,
                text=_fmt_val(initial),
                bg=tokens.BG_MUTED,
                fg=tokens.FG_2,
                font=_tkfont.Font(family="Segoe UI", size=tokens.FS_12),
                width=8,
                anchor="w",
            )
            _value_lbl.pack(side="right", padx=(tokens.SP_2, 0))

            scale_var.trace_add("write", _on_scale_change)

        # Store the scale widget itself so _set_inputs_state can disable it.
        container._scale = scale  # type: ignore[attr-defined]
        # _scale_var exposed to tests: actual_var (numeric_box) or scale_var (plain).
        container._scale_var = _scale_var_exposed  # type: ignore[attr-defined]
        # Keep _value_lbl for backward compat with existing tests (None when numeric_box=True).
        container._value_lbl = _value_lbl  # type: ignore[attr-defined]
        # Expose numeric entry for tests / state management.
        container._num_entry = _num_entry  # type: ignore[attr-defined]
        container._num_entry_var = _num_entry_var  # type: ignore[attr-defined]
        # Expose actual_var for decoupled-var tests (None when numeric_box=False).
        container._actual_var = _actual_var  # type: ignore[attr-defined]

        return container

