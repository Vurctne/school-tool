    def _clear_results(self, tid: str) -> None:
        wmap = self._tool_widgets[tid]

        # Clear banner
        banner_frame = wmap.get("__banner_frame__")
        if banner_frame:
            for child in banner_frame.winfo_children():
                child.destroy()
        self._tool_banners[tid] = None

        # Clear metrics
        metric_frame = self._tool_metric_frames.get(tid)
        if metric_frame:
            for child in metric_frame.winfo_children():
                child.destroy()
            metric_frame.pack_forget()

        # Clear log
        lv = self._tool_logviews.get(tid)
        if lv:
            lv.clear()
        log_frame = wmap.get("__log_frame__")
        if log_frame:
            log_frame.pack_forget()

        # Clear table
        table_frame = wmap.get("__table_frame__")
        if table_frame:
            for child in table_frame.winfo_children():
                child.destroy()
            table_frame.pack_forget()
        self._tool_tables[tid] = None

    def _clear_tool(self, tool: BaseTool) -> None:
        """Handle the Clear button click: reset all UI state for the tool,
        then delegate per-tool state to tool.clear().

        UI-level resets handled here:
          1. File picker widget values + input cache cleared.
          2. Banner frame emptied.
          3. Progress bar frame hidden.
          4. Metrics frame hidden and emptied.
          5. Log view cleared and hidden.
          6. Table frame destroyed and hidden.
          7. Primary button re-enabled.
          8. Active filter reset + filter chip hidden.
          9. tool.clear() called for per-tool state.
        """
        tid = tool.id
        wmap = self._tool_widgets[tid]

        # 1. Clear file picker widgets and input cache
        for inp in tool.inputs:
            w = wmap.get(inp.key)
            if w is not None and isinstance(w, FileRow):
                w._var.set("")  # clear the displayed path
        out_row = self._tool_output_rows.get(tid)
        if out_row is not None:
            out_row._var.set("")  # clear the output file path
        self._input_cache[tid] = {}

        # 2. Clear banner
        banner_frame = wmap.get("__banner_frame__")
        if banner_frame:
            for child in banner_frame.winfo_children():
                child.destroy()
        self._tool_banners[tid] = None

        # 3. Hide progress bar
        pb_frame = wmap.get("__pb_frame__")
        if pb_frame:
            pb_frame.pack_forget()
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(0, "")

        # 4. Hide and empty metrics
        metric_frame = self._tool_metric_frames.get(tid)
        if metric_frame:
            for child in metric_frame.winfo_children():
                child.destroy()
            metric_frame.pack_forget()

        # 5. Clear and hide log
        lv = self._tool_logviews.get(tid)
        if lv:
            lv.clear()
        log_frame = wmap.get("__log_frame__")
        if log_frame:
            log_frame.pack_forget()

        # 6. Reset active filter BEFORE destroying table children.
        #
        # Order matters: the SelectableList (rail) lives inside table_frame's
        # child grid_frame.  If we destroyed table_frame's children first,
        # rail.set_active(None) would fire configure() calls against already-
        # destroyed Tk widgets and raise "invalid command name …" errors.
        # Clearing the rail state here — while the widgets are still alive —
        # prevents that stale-callback error.
        self._tool_filter[tid] = None
        self._tool_table_specs[tid] = None
        rail = self._tool_rails.get(tid)
        if rail is not None:
            # Guard with winfo_exists() in case the widget was destroyed by
            # an earlier pack_forget/GC cycle (belt-and-suspenders).
            try:
                if rail.winfo_exists():
                    rail.set_active(None)
            except tk.TclError:
                pass
        self._tool_rails[tid] = None

        # Destroy table children and hide (safe now — rail state cleared above).
        table_frame = wmap.get("__table_frame__")
        if table_frame:
            for child in table_frame.winfo_children():
                child.destroy()
            table_frame.pack_forget()
        self._tool_tables[tid] = None

        # 7. Re-enable primary button
        primary = self._tool_primary_btns.get(tid)
        if primary:
            primary.configure(state="normal")

        # 8. Hide the filter chip (filter state already cleared above in step 6).
        chip_frame = self._tool_filter_chips.get(tid)
        if chip_frame is not None:
            for child in chip_frame.winfo_children():
                try:
                    child.destroy()
                except tk.TclError:
                    pass
            chip_frame.pack_forget()

        # 9. Delegate per-tool state reset
        tool.clear()

        self._set_status("Ready")
        logger.info("Cleared tool: %s (%s)", tool.label, tid)

    def _set_inputs_state(self, tid: str, state: str) -> None:
        """Enable or disable all input widgets and the primary button for a tool."""
        wmap = self._tool_widgets[tid]
        tool = self._tool_map[tid]

        for inp in tool.inputs:
            w = wmap.get(inp.key)
            if w is None:
                continue
            if isinstance(w, FileRow):
                w.set_state(state)
            elif isinstance(inp, RangeInput):
                # RangeInput renders as a tk.Frame container; the actual
                # ttk.Scale is stored as container._scale.  The optional
                # numeric entry (_num_entry) is disabled alongside it.
                scale_widget = getattr(w, "_scale", None)
                if scale_widget is not None:
                    try:
                        scale_widget.configure(state=state)
                    except tk.TclError:
                        pass
                num_entry = getattr(w, "_num_entry", None)
                if num_entry is not None:
                    try:
                        num_entry.configure(state=state)
                    except tk.TclError:
                        pass
            else:
                try:
                    w.configure(state=state)
                except tk.TclError:
                    pass

        out_row = self._tool_output_rows.get(tid)
        if out_row:
            out_row.set_state(state)

        primary = self._tool_primary_btns.get(tid)
        if primary:
            primary.configure(state=state)

    # -----------------------------------------------------------------------
    # Global exception handler
    # ----------------------------------------------------------------------
    def _handle_tk_exception(
        self,
        exc_type: type[BaseException],
        exc_value: BaseException,
        exc_tb: Any,
    ) -> None:
        tb_str = "".join(traceback.format_exception(exc_type, exc_value, exc_tb))
        logger.error("Unhandled exception in Tk callback:\n%s", tb_str)

        msg = str(exc_value)
        log_hint = f"\nSee log at: {self._log_path}" if self._log_path else ""

        # Show a danger banner in the active tool frame
        if self._active_id and self._active_id in self._tool_widgets:
            wmap = self._tool_widgets[self._active_id]
            banner_frame = wmap.get("__banner_frame__")
            if banner_frame:
                for child in banner_frame.winfo_children():
                    child.destroy()
                banner = Banner(
                    banner_frame,
                    level="danger",
                    text=f"An unexpected error occurred: {msg}{log_hint}",
                )
                banner.pack(fill="x")

    # -----------------------------------------------------------------------
    # Root-window resize debounce + drag-state suspension (Fix 2/Round 13,
    # Fix 5/Round 17)
    # -----------------------------------------------------------------------

    def _on_root_configure(self, _event: Any) -> None:
        """Debounce handler for the root window's <Configure> event.

        Round 13: batches root Configure events into a single deferred call
        ~100 ms after the last event, preventing layout thrash.

        Round 17 (Fix 5): additionally sets ``_drag_active = True`` so that
        canvas Configure handlers (in _build_tool_frame and SelectableList)
        can short-circuit entirely during the drag, skipping even the after()
        scheduling overhead.  ``_on_drag_settled`` runs 150 ms after the last
        event and: (a) clears _drag_active, (b) fires all pending canvas
        configure callbacks once, (c) runs the root settle (no-op today).

        The 150 ms settle window is deliberately longer than the inner canvas
        debounce (100 ms) so it acts as the outer gate.
        """
        # Mark drag as active -- canvas handlers will skip during this window
        self._drag_active = True

        # Cancel-and-reschedule the settle timer
        if self._drag_settle_after is not None:
            try:
                self._root.after_cancel(self._drag_settle_after)
            except tk.TclError:
                pass
        self._drag_settle_after = self._root.after(150, self._on_drag_settled)

        # Also maintain the legacy root_configure_after (shorter window used by
        # callers that want an early settle signal)
        if self._root_configure_after is not None:
            try:
                self._root.after_cancel(self._root_configure_after)
            except tk.TclError:
                pass
        self._root_configure_after = self._root.after(100, self._do_root_configure)

    def _do_root_configure(self) -> None:
        """Deferred root-configure settle -- runs once per 100 ms idle window."""
        self._root_configure_after = None
        # No-op for now; the method exists so future global resize logic can be
        # added here without introducing a new un-debounced Configure binding.

    def _on_drag_settled(self) -> None:
        """Fires 150 ms after the last root Configure event.

        Clears _drag_active and fires any pending canvas-configure callbacks
        that were skipped during the drag.  This ensures the layout is correct
        after the user releases the window edge, without having done unnecessary
        work on every intermediate pixel.
        """
        self._drag_settle_after = None
        self._drag_active = False

        # Fire all pending canvas-configure callbacks accumulated during drag
        pending = self._pending_canvas_configures[:]
        self._pending_canvas_configures.clear()
        for cb in pending:
            try:
                cb()
            except Exception:  # noqa: BLE001
                pass

