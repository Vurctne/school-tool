    def _activate_tool(self, tool_id: str) -> None:
        if tool_id not in self._tool_frames:
            return

        self._active_id = tool_id
        tool = self._tool_map[tool_id]

        # Update rail selection styling
        for tid, lbl in self._rail_items.items():
            if tid == tool_id:
                lbl.configure(bg=tokens.RAIL_SELECTED, fg=tokens.FG_INVERSE)
            else:
                lbl.configure(bg=tokens.BG_PANEL, fg=tokens.FG_1)

        # Update root window title
        self._root.title(f"{APP_TITLE} — {tool.label}")

        # Update in-app header: chip reflects active tool's short mark + title text
        self._header_chip.configure(text=tool.short)
        self._header_title_var.set(f"{APP_TITLE} — {tool.label}")

        # Licence gate — check before lifting the tool frame
        feature = getattr(tool, "requires_feature", None)
        if feature is not None:
            import toolkit.licence as _licence  # noqa: PLC0415  (lazy import)

            status = _licence.read_status()
            if status.state not in {"active", "grace"}:
                self._show_unlock_cta(tool)
                self._set_status("Ready")
                logger.info("Licence gate blocked tool: %s (%s)", tool.label, tool_id)
                return
            if status.state == "grace":
                logger.warning(
                    "Licence in grace period — allowing tool: %s (%s)",
                    tool.label,
                    tool_id,
                )

        # Show/hide content frames — use place_forget/place instead of lower/lift
        # so that inactive frames are fully removed from the geometry manager.
        # Placed frames with relwidth/relheight receive <Configure> from Tk even
        # when hidden (lowered), multiplying resize work by the tool count.
        # (Fix 2 — Round 13)
        for tid, frame in self._tool_frames.items():
            if tid != tool_id:
                frame.place_forget()
        if self._unlock_frame is not None:
            self._unlock_frame.place_forget()
        if self._user_frame is not None:
            self._user_frame.place_forget()
        self._tool_frames[tool_id].place(
            in_=self._content_outer, x=0, y=0, relwidth=1.0, relheight=1.0
        )

        self._set_status("Ready")
        logger.info("Activated tool: %s (%s)", tool.label, tool_id)

