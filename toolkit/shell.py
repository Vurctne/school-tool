        # Hide all tool frames AND the unlock CTA before lifting the user frame.
        for frame in self._tool_frames.values():
            frame.lower()
        if self._unlock_frame is not None:
            self._unlock_frame.lower()
        self._user_frame.lift()

        # Update title and SF chip
        self._root.title(f"{APP_TITLE} \u2014 User")
        self._header_chip.configure(text="US")
        self._header_title_var.set(f"{APP_TITLE} \u2014 User")

        # De-select any highlighted tool in the rail
        for lbl in self._rail_items.values():
            lbl.configure(bg=tokens.BG_PANEL, fg=tokens.FG_1)

        self._set_status("Ready")
        self._user_frame.refresh()
        logger.info("Activated User tab")

