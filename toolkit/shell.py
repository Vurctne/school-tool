        # already renders a navy title bar so the duplicate is noisy.
        lines = raw.splitlines()
        if lines and lines[0].lstrip().startswith("# "):
            lines = lines[1:]
            # Also strip the immediate blank line that usually follows.
            if lines and not lines[0].strip():
                lines = lines[1:]
        body = "\n".join(lines)

        self._show_help_window("Privacy Policy", body)

