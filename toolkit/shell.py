def _filter_rows(rows: list[dict[str, Any]], filter_key: str) -> list[dict[str, Any]]:
    """Return the subset of *rows* whose ``_faculty`` value equals *filter_key*.

    Pure function — no Tk dependency. Extracted so tests can exercise the
    filter logic on Linux CI where Tkinter is absent.
    """
    return [r for r in rows if r.get("_faculty") == filter_key]


