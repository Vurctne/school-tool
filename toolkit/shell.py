    def _run_tool(self, tool: BaseTool) -> None:
        tid = tool.id

        # Gather input values from widgets
        wmap = self._tool_widgets[tid]
        resolved: dict[str, Any] = {}

        for inp in tool.inputs:
            w = wmap.get(inp.key)
            if w is None:
                continue
            if isinstance(inp, FileInput) and isinstance(w, FileRow):
                p = w.get_path()
                if p:
                    resolved[inp.key] = p
                    self._input_cache[tid][inp.key] = p
            elif isinstance(inp, TextInput) and isinstance(w, TextField):
                resolved[inp.key] = w.get()
                self._input_cache[tid][inp.key] = w.get()
            elif isinstance(inp, NumberInput) and isinstance(w, NumberField):
                from decimal import Decimal

                raw = w.get()
                try:
                    resolved[inp.key] = Decimal(raw) if inp.decimals else int(raw)
                except Exception:
                    resolved[inp.key] = raw
                self._input_cache[tid][inp.key] = raw
            elif isinstance(inp, CurrencyInput) and isinstance(w, CurrencyField):
                from decimal import Decimal

                raw = w.get()
                try:
                    resolved[inp.key] = Decimal(raw)
                except Exception:
                    resolved[inp.key] = Decimal("0")
                self._input_cache[tid][inp.key] = raw
            elif isinstance(inp, DateInput) and isinstance(w, DateField):
                resolved[inp.key] = w.get_date()
                self._input_cache[tid][inp.key] = w.get_date()
            elif isinstance(inp, SecretInput) and isinstance(w, SecretField):
                resolved[inp.key] = w.get()
            elif isinstance(inp, RangeInput):
                # Value is always kept current in input_cache by the slider trace.
                cached_range = self._input_cache[tid].get(inp.key, inp.default)
                try:
                    resolved[inp.key] = float(cached_range)
                except (TypeError, ValueError):
                    resolved[inp.key] = inp.default
                self._input_cache[tid][inp.key] = resolved[inp.key]

        # Add output path if present
        out_row = self._tool_output_rows.get(tid)
        if out_row and tool.output:
            out_path = out_row.get_path()
            if out_path:
                resolved[tool.output.key] = out_path

        # Disable inputs + show progress bar
        self._set_inputs_state(tid, "disabled")
        pb_frame = wmap.get("__pb_frame__")
        if pb_frame:
            pb_frame.pack(fill="x", padx=tokens.SP_4, pady=(tokens.SP_2, 0))
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(0, "Starting…")

        # Clear previous results
        self._clear_results(tid)

        self._set_status("Running…")
        logger.info("Starting tool: %s", tool.label)

        executor = self._executors[tid]

        def _progress_worker(pct: int, msg: str) -> None:
            self._root.after(0, lambda: self._on_progress(tid, pct, msg))

        def _worker() -> ToolResult:
            return tool.run(resolved, _progress_worker)

        future: Future[ToolResult] = executor.submit(_worker)
        future.add_done_callback(
            lambda f: self._root.after(0, lambda: self._on_tool_complete(tid, f))
        )

    def _on_progress(self, tid: str, pct: int, msg: str) -> None:
        if tid != self._active_id:
            return
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(pct, msg)
        self._set_status(msg)

    def _on_tool_complete(self, tid: str, future: Future[ToolResult]) -> None:
        # Re-enable inputs
        self._set_inputs_state(tid, "normal")

        exc = future.exception()
        if exc is not None:
            logger.exception("Tool %s raised an exception", tid, exc_info=exc)
            # Round 20 — translate uncaught exceptions to plain English so
            # users see something actionable instead of a Python type name.
            from toolkit.user_errors import friendly_error

            fe = friendly_error(exc) if isinstance(exc, Exception) else None
            if fe is not None:
                from toolkit.base_tool import LogLine

                result = ToolResult(
                    status="error",
                    banner_level="danger",
                    banner_text=fe.banner,
                    log_lines=[
                        LogLine("WHAT WENT WRONG", tag="heading"),
                        LogLine(fe.message, tag="danger"),
                        LogLine("HOW TO FIX IT", tag="heading"),
                        LogLine(fe.advice, tag="muted"),
                        LogLine("TECHNICAL DETAIL (for support)", tag="heading"),
                        LogLine(fe.technical, tag="muted"),
                    ],
                )
            else:
                result = ToolResult(
                    status="error",
                    banner_level="danger",
                    banner_text="Something unexpected went wrong.",
                )
        else:
            result = future.result()

        self._render_result(tid, result)

        # Hide progress bar
        wmap = self._tool_widgets[tid]
        pb_frame = wmap.get("__pb_frame__")
        if pb_frame:
            pb_frame.pack_forget()
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(100, result.banner_text[:80])

        self._set_status(result.banner_text[:100])
        logger.info("Tool %s completed with status %s", tid, result.status)

    def _render_result(self, tid: str, result: ToolResult) -> None:
        wmap = self._tool_widgets[tid]
        banner_frame = wmap.get("__banner_frame__")

        # Banner
        if banner_frame:
            for child in banner_frame.winfo_children():
                child.destroy()
            banner = Banner(banner_frame, level=result.banner_level, text=result.banner_text)
            banner.pack(fill="x")
            self._tool_banners[tid] = banner

        # Metrics
        metric_frame = self._tool_metric_frames.get(tid)
        if metric_frame:
            for child in metric_frame.winfo_children():
                child.destroy()
            if result.metrics:
                large_single = len(result.metrics) == 1
                metric_frame.pack(fill="x", padx=tokens.SP_4, pady=(tokens.SP_2, 0))
                row = tk.Frame(metric_frame, bg=tokens.BG_MUTED)
                row.pack(fill="x")
                for lbl, val, tone in result.metrics:
                    m = Metric(
                        row,
                        label=lbl,
                        value=val,
                        tone=tone or "neutral",
                        large=large_single,
                    )
                    m.pack(side="left", padx=(0, tokens.SP_3), expand=True)

        # Log lines
        lv = self._tool_logviews.get(tid)
        log_frame = wmap.get("__log_frame__")
        if lv and result.log_lines:
            lv.clear()
            for line in result.log_lines:
                lv.append(line)
            if log_frame:
                log_frame.pack(fill="both", expand=True, padx=tokens.SP_4, pady=(tokens.SP_2, 0))

        # Reset filter state when a fresh ToolResult arrives
        self._tool_filter[tid] = None
        self._tool_table_specs[tid] = None
        self._tool_rails[tid] = None
        # Hide any stale chip
        chip_frame_ref = wmap.get("__chip_frame__")
        if chip_frame_ref is not None:
            for child in chip_frame_ref.winfo_children():
                child.destroy()
            chip_frame_ref.pack_forget()

        # Table (and optional side rail)
        table_frame = wmap.get("__table_frame__")
        if table_frame:
            # Determine whether we have any table content to show
            has_table_spec = result.table is not None
            has_legacy_table = result.table_columns is not None and result.table_rows is not None

            if has_table_spec or has_legacy_table:
                # Destroy any previously-rendered content
                for child in table_frame.winfo_children():
                    child.destroy()

                if result.side_rail is not None:
                    # -------------------------------------------------------
                    # Two-column layout: rail (220 px) | table (fill)
                    # -------------------------------------------------------
                    grid_frame = tk.Frame(table_frame, bg=tokens.BG_MUTED)
                    grid_frame.pack(fill="both", expand=True)

                    def _on_rail_select(filter_key: str, tool_id: str = tid) -> None:
                        self._apply_filter(tool_id, filter_key)

                    rail = SelectableList(
                        grid_frame,
                        items=result.side_rail,
                        on_select=_on_rail_select,
                        focus_on_mount=False,
                        width=220,
                    )
                    rail.grid(
                        row=0,
                        column=0,
                        sticky="ns",
                        padx=(0, tokens.SP_GAP_SECTION),
                    )

                    tbl = self._build_table_widget(grid_frame, result)
                    tbl.grid(row=0, column=1, sticky="nsew")

                    grid_frame.grid_columnconfigure(0, weight=0, minsize=220)
                    grid_frame.grid_columnconfigure(1, weight=1)
                    grid_frame.grid_rowconfigure(0, weight=1)

                    # Cache for filter operations
                    self._tool_rails[tid] = rail
                    self._tool_table_specs[tid] = result.table
                else:
                    # -------------------------------------------------------
                    # Single-column layout — preserved from today
                    # -------------------------------------------------------
                    tbl = self._build_table_widget(table_frame, result)
                    tbl.pack(fill="both", expand=True)

                table_frame.pack(fill="both", expand=True, padx=tokens.SP_4, pady=(tokens.SP_2, 0))
                self._tool_tables[tid] = tbl

    def _build_table_widget(self, parent: tk.Widget, result: ToolResult) -> Table:
        """Construct and populate a Table widget from a ToolResult.

        Supports two source paths:
        - ``result.table`` (TableSpec) — new path; forwards row_style and on_row_click.
        - ``result.table_columns`` + ``result.table_rows`` — legacy path; preserved exactly.

        The caller is responsible for placing (pack/grid) the returned widget.
        """
        if result.table is not None:
            spec: TableSpec = result.table
            tbl = Table(
                parent,
                columns=spec.columns,
                row_style=spec.row_style,
                on_row_click=spec.on_row_click,
            )
            tbl.set_rows(spec.rows)
        else:
            # Legacy path — table_columns / table_rows (already guarded by caller)
            cols = result.table_columns or []
            rows = result.table_rows or []
            tbl = Table(parent, columns=cols)
            tbl.set_rows(rows)
        return tbl

