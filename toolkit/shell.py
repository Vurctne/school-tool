    def _show_help_window(self, title: str, help_text: str) -> None:
        """Render ``help_text`` in a dedicated scrollable modal window with a
        navy header strip, heading/mono tagging, and live colour swatches for
        any ``#RRGGBB`` hex codes in the body.
        """
        # Build a dedicated modal window with a scrollable read-only Text widget.
        win = tk.Toplevel(self._root)
        win.title(title)
        win.geometry("720x640")
        win.minsize(560, 400)
        win.transient(self._root)
        win.configure(bg=tokens.BG_PANEL)

        # Header strip (navy band with the tool label)
        header = tk.Frame(win, bg=tokens.BRAND_NAVY, height=48)
        header.pack(side="top", fill="x")
        header.pack_propagate(False)
        tk.Label(
            header,
            text=title,
            bg=tokens.BRAND_NAVY,
            fg=tokens.FG_INVERSE,
            font=tkfont.Font(family=self._fonts.sans_family, size=tokens.FS_14, weight="bold"),
            anchor="w",
            padx=tokens.SP_4,
        ).pack(fill="both", expand=True)

        # Body: scrollable Text
        body = tk.Frame(win, bg=tokens.BG_PANEL)
        body.pack(side="top", fill="both", expand=True, padx=tokens.SP_4, pady=tokens.SP_3)

        yscroll = ttk.Scrollbar(body, orient="vertical")
        yscroll.pack(side="right", fill="y")

        text = tk.Text(
            body,
            wrap="word",
            bg=tokens.BG_PANEL,
            fg=tokens.FG_1,
            relief="flat",
            borderwidth=0,
            padx=tokens.SP_3,
            pady=tokens.SP_3,
            font=tkfont.Font(family=self._fonts.sans_family, size=tokens.FS_13),
            yscrollcommand=yscroll.set,
        )
        yscroll.configure(command=text.yview)
        text.pack(side="left", fill="both", expand=True)

        # Tag configuration for emphasis
        text.tag_configure(
            "heading",
            font=tkfont.Font(family=self._fonts.sans_family, size=tokens.FS_14, weight="bold"),
            foreground=tokens.BRAND_NAVY,
            spacing1=12,
            spacing3=4,
        )
        text.tag_configure(
            "mono",
            font=tkfont.Font(family=self._fonts.mono_family, size=tokens.FS_13),
        )

        # Any "#RRGGBB" hex code in the body gets a live colour swatch
        # embedded inline (so the reader sees the actual colour, not just
        # the code).
        import re as _re

        hex_re = _re.compile(r"#[0-9A-Fa-f]{6}\b")

        def _insert_line_with_swatches(raw_line: str, tag: str | None) -> None:
            """Insert ``raw_line`` into the Text widget. Every ``#RRGGBB`` hex
            code is **replaced** by a small colour rectangle (the user sees the
            actual colour, not the code)."""
            pos = 0
            for match in hex_re.finditer(raw_line):
                start, end = match.span()
                if start > pos:
                    segment = raw_line[pos:start]
                    if tag:
                        text.insert("end", segment, tag)
                    else:
                        text.insert("end", segment)
                hex_code = match.group(0).upper()
                # Small 14x12 swatch with a subtle 1 px border.
                swatch = tk.Frame(
                    text,
                    bg=hex_code,
                    width=14,
                    height=12,
                    highlightthickness=1,
                    highlightbackground=tokens.BORDER_SUBTLE,
                )
                swatch.pack_propagate(False)
                text.window_create("end", window=swatch, padx=0, pady=0)
                # Hex code itself is intentionally NOT inserted — the swatch
                # alone communicates the colour; showing raw codes clutters
                # the prose.
                pos = end
            tail = raw_line[pos:] + "\n"
            if tag:
                text.insert("end", tail, tag)
            else:
                text.insert("end", tail)

        # Lines in ALL-CAPS are treated as headings; indented lines (4 spaces)
        # are treated as monospace blocks.
        for line in help_text.splitlines():
            stripped = line.strip()
            is_heading = bool(stripped) and stripped == stripped.upper() and any(
                c.isalpha() for c in stripped
            ) and len(stripped) > 2
            is_mono = line.startswith("    ")
            if is_heading:
                _insert_line_with_swatches(line, "heading")
            elif is_mono:
                _insert_line_with_swatches(line, "mono")
            else:
                _insert_line_with_swatches(line, None)

        text.configure(state="disabled")  # read-only

        # Footer: Close button
        footer = tk.Frame(win, bg=tokens.BG_PANEL)
        footer.pack(side="bottom", fill="x", padx=tokens.SP_4, pady=tokens.SP_3)
        ttk.Button(footer, text="Close", command=win.destroy).pack(side="right")

        # Centre on the main window, modal-style focus
        win.update_idletasks()
        x = self._root.winfo_rootx() + (self._root.winfo_width() - win.winfo_width()) // 2
        y = self._root.winfo_rooty() + (self._root.winfo_height() - win.winfo_height()) // 2
        win.geometry(f"+{max(x, 0)}+{max(y, 0)}")
        win.grab_set()
        win.focus_set()

