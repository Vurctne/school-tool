    def _build_rail(self, parent: tk.Widget | None = None) -> tk.Frame:
        """Build the 220 px left rail with grouped tool entries.

        ``parent`` defaults to the body frame for backward compatibility, but
        the live shell now wraps the rail in a ``tk.PanedWindow`` and passes
        that PanedWindow in so the user can drag the rail width (Round 19).
        """
        rail = tk.Frame(
            parent if parent is not None else self._body_frame,
            bg=tokens.BG_PANEL,
            width=220,
        )
        rail.pack_propagate(False)

        # 1 px right border
        border = tk.Frame(rail, bg=tokens.BORDER_SUBTLE, width=1)
        border.pack(side="right", fill="y")

        scroll_container = tk.Frame(rail, bg=tokens.BG_PANEL)
        scroll_container.pack(side="left", fill="both", expand=True, pady=tokens.SP_1)

        # Group tools
        groups: dict[str, list[BaseTool]] = {}
        for tool in sorted(self._tools, key=lambda t: (t.group, t.order, t.label)):
            groups.setdefault(tool.group, []).append(tool)

        self._rail_items: dict[str, tk.Label] = {}

        for group_name, group_tools in groups.items():
            # Group header
            grp_lbl = tk.Label(
                scroll_container,
                text=f"▾ {group_name}",
                bg=tokens.BG_PANEL,
                fg=tokens.FG_1,
                font=self._group_header_font(),
                anchor="w",
                padx=tokens.SP_2,
                pady=tokens.SP_1,
            )
            grp_lbl.pack(fill="x")

            for tool in group_tools:
                item = tk.Label(
                    scroll_container,
                    text=tool.label,
                    bg=tokens.BG_PANEL,
                    fg=tokens.FG_1,
                    font=self._rail_font(),
                    anchor="w",
                    padx=30,
                    pady=tokens.SP_1,
                    cursor="hand2",
                )
                item.pack(fill="x")
                tid = tool.id
                item.bind("<Button-1>", lambda _e, t=tid: self._activate_tool(t))  # type: ignore[misc]
                item.bind("<Enter>", lambda _e, w=item, t=tid: self._rail_hover(w, t))  # type: ignore[misc]
                item.bind("<Leave>", lambda _e, w=item, t=tid: self._rail_unhover(w, t))  # type: ignore[misc]
                self._rail_items[tool.id] = item

        # "In development" section — non-clickable placeholders for tools that
        # are designed but not yet shipped, or temporarily parked.
        from toolkit.registry import IN_DEVELOPMENT_TOOLS

        if IN_DEVELOPMENT_TOOLS:
            indev_header = tk.Label(
                scroll_container,
                text="In development",
                bg=tokens.BG_PANEL,
                fg=tokens.FG_3,
                font=self._group_header_font(),
                anchor="w",
                padx=tokens.SP_2,
                pady=tokens.SP_1,
            )
            indev_header.pack(fill="x")

            for _group, label, _short, _reason in IN_DEVELOPMENT_TOOLS:
                item = tk.Label(
                    scroll_container,
                    text=label,
                    bg=tokens.BG_PANEL,
                    fg=tokens.FG_3,
                    font=self._rail_font(),
                    anchor="w",
                    padx=30,
                    pady=tokens.SP_1,
                )
                item.pack(fill="x")
                # No <Button-1> binding — these entries are non-clickable.

        # Divider
        div = tk.Frame(scroll_container, bg=tokens.BORDER_SUBTLE, height=1)
        div.pack(fill="x", padx=tokens.SP_3, pady=(tokens.SP_2, tokens.SP_1))

        # Static entries: Instructions, Privacy Policy. User gated by SHOW_USER_TAB.
        # Round 21 — replaced "About" (basic version + email blurb) with
        # "Privacy Policy" because that's what users actually need a link
        # to. The privacy text is loaded from docs/store_privacy_policy.md
        # and rendered using the same scrollable help-window primitive used
        # for tool instructions.
        static_entries: list[tuple[str, Any]] = [
            ("Instructions", self._show_instructions),
            ("Privacy Policy", self._show_privacy_policy),
        ]
        if app_metadata.SHOW_USER_TAB:
            static_entries.append(("User", self._show_user))

        for entry_label, callback in static_entries:
            lbl = tk.Label(
                scroll_container,
                text=entry_label,
                bg=tokens.BG_PANEL,
                fg=tokens.FG_1,
                font=self._rail_font(),
                anchor="w",
                padx=tokens.SP_3,
                pady=3,
                cursor="hand2",
            )
            lbl.pack(fill="x")
            lbl.bind("<Button-1>", lambda _e, cb=callback: cb())  # type: ignore[misc]

        return rail

    def _build_tool_frame(self, tool: BaseTool) -> tuple[tk.Frame, _WidgetMap]:
        """Build the full content frame for a single tool. Returns (frame, widget_map)."""
        frame = tk.Frame(self._content_outer, bg=tokens.BG_MUTED)
        widget_map: _WidgetMap = {}

        # Scrollable canvas for the content
        canvas = tk.Canvas(frame, bg=tokens.BG_MUTED, highlightthickness=0)
        vsb = ttk.Scrollbar(frame, orient="vertical", command=canvas.yview)
        canvas.configure(yscrollcommand=vsb.set)

        vsb.pack(side="right", fill="y")
        canvas.pack(side="left", fill="both", expand=True)

        inner = tk.Frame(canvas, bg=tokens.BG_MUTED)
        canvas_window = canvas.create_window((0, 0), window=inner, anchor="nw")

        # Debounce after() IDs — cancel-and-reschedule so layout math runs only
        # once per ~100 ms idle window, not on every pixel of a window-resize drag.
        # 100 ms (raised from 50 ms in Round 14) reduces flicker frequency at the
        # cost of slightly slower settle; the bigger win is that inactive tool frames
        # are place_forget()-ed so they receive no Configure events at all.
        # This matches the pattern used in SelectableList (Round 9 fix).
        _inner_configure_after: list[str | None] = [None]
        _canvas_configure_after: list[str | None] = [None]

        def _do_inner_configure() -> None:
            # Fired only when the INNER FRAME's content changes (children
            # rearranging or fonts changing) — not on every window resize.
            # scrollregion tracks the inner content's natural bbox; it does NOT
            # need updating when only the canvas width changes.  (Round 14)
            _inner_configure_after[0] = None
            try:
                canvas.configure(scrollregion=canvas.bbox("all"))
            except tk.TclError:
                pass

        def _on_inner_configure(_e: Any) -> None:
            if _inner_configure_after[0] is not None:
                try:
                    canvas.after_cancel(_inner_configure_after[0])
                except tk.TclError:
                    pass
            _inner_configure_after[0] = canvas.after(100, _do_inner_configure)

        def _do_canvas_configure(w: int) -> None:
            # Fired only when the CANVAS width changes (window resize).
            # Updates the inner window width so horizontal content stretches.
            # scrollregion is NOT recomputed here — that's the inner-frame's job.
            # (Round 14)
            _canvas_configure_after[0] = None
            try:
                canvas.itemconfigure(canvas_window, width=w)
            except tk.TclError:
                pass

        def _on_canvas_configure(e: Any) -> None:
            w = e.width
            # Fix 5 (Round 17): during an active window-edge drag skip all
            # intermediate layout work — record the latest width as a pending
            # callback and return immediately.  _on_drag_settled() will fire it
            # once 150 ms after the drag ends.  This eliminates the after()
            # scheduling overhead on every pixel of a resize drag.
            if self._drag_active:
                # Replace any previously-queued pending callback for this canvas
                # so only the last width is used when drag settles.
                cb = lambda: _do_canvas_configure(w)  # noqa: E731
                # Remove any earlier pending entry for this canvas (identified by
                # canvas identity) by filtering on the closure's __code__ object
                # is not reliable — we use a sentinel list instead.
                self._pending_canvas_configures.append(cb)
                return
            if _canvas_configure_after[0] is not None:
                try:
                    canvas.after_cancel(_canvas_configure_after[0])
                except tk.TclError:
                    pass
            _canvas_configure_after[0] = canvas.after(100, lambda: _do_canvas_configure(w))

        inner.bind("<Configure>", _on_inner_configure)
        canvas.bind("<Configure>", _on_canvas_configure)

        pad = tokens.SP_4  # 16 px
        gap = tokens.SP_GAP_SECTION  # 10 px per design spec

        def section(pady_top: int = 0) -> tk.Frame:
            f = tk.Frame(inner, bg=tokens.BG_MUTED)
            f.pack(fill="x", padx=pad, pady=(pady_top, 0))
            return f

        # Section header
        hdr_frame = section(pad)
        sh = SectionHeader(hdr_frame, title=tool.label)
        sh.pack(fill="x")

        has_output = tool.output is not None

        # Input fields
        for inp in tool.inputs:
            inp_frame = section(gap)
            w = self._build_input_widget(inp_frame, inp, tool)
            w.pack(fill="x")
            widget_map[inp.key] = w

        # Output file row (only when tool.output is set)
        output_row: FileRow | None = None
        if has_output and tool.output is not None:
            out_spec: OutputSpec = tool.output
            out_frame = section(gap)
            out_row_label = out_spec.label or "Output file"

            def _on_output_pick(p: Path, key: str = out_spec.key) -> None:
                self._input_cache[tool.id][key] = p

            output_row = FileRow(
                out_frame,
                label=out_row_label,
                on_pick=_on_output_pick,
                filetypes=[
                    (f"{out_spec.suffix.lstrip('.')} files", f"*{out_spec.suffix}"),
                    ("All files", "*.*"),
                ],
                optional=False,
            )
            output_row.pack(fill="x")

        self._tool_output_rows[tool.id] = output_row

        # --- Action button row ---
        btn_frame = section(gap + tokens.SP_1)
