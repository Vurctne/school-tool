"""TkShell — the top-level window chrome, rail, status bar, and tool runner.

Responsibilities
----------------
* Win11-style title bar (title text + SF monogram chip)
* Left rail (220 px) — groups from BaseTool.group, sorted (group, order, label)
  plus static Instructions / About / User rows below a divider.
* Status bar (22 px) — live status left, version + email right.
* Content area — auto-generates each tool's frame from tool.inputs dispatched
  to the right primitive, plus output FileRow when tool.output is set.
* Primary button — disables inputs, spawns worker via ThreadPoolExecutor,
  marshals progress/completion to Tk via root.after(0, ...).
* Secondary buttons from tool.secondary_actions().
* Tool switching preserves per-tool input values in memory.
* Global exception handler → danger banner + log path.
"""

