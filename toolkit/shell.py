    def _build_rail(self) -> tk.Frame:
        """Build the 220 px left rail with grouped tool entries."""
        rail = tk.Frame(
            self._body_frame,
            bg=tokens.BG_PANEL,
            width=220,
        )
        rail.pack_propagate(False)

        # 1 px right border
        border = tk.Frame(rail, bg=tokens.BORDER_SUBTLE, width=1)
        border.pack(side="right", fill="y")

        scroll_container = tk.Frame(rail, bg=tokens.BG_PANEL)
        scroll_container.pack(side="left", fill="both", expand=True, pady=tokens.SP_1)

        # Group tools
        groups: dict[str, list[BaseTool]] = {}
        for tool in sorted(self._tools, key=lambda t: (t.group, t.order, t.label)):
            groups.setdefault(tool.group, []).append(tool)

        self._rail_items: dict[str, tk.Label] = {}

        for group_name, group_tools in groups.items():
            # Group header
            grp_lbl = tk.Label(
                scroll_container,
                text=f"\u25be {group_name}",
                bg=tokens.BG_PANEL,
                fg=tokens.FG_1,
                font=self._group_header_font(),
                anchor="w",
                padx=tokens.SP_2,
                pady=tokens.SP_1,
            )
            grp_lbl.pack(fill="x")

            for tool in group_tools:
                item = tk.Label(
                    scroll_container,
                    text=tool.label,
                    bg=tokens.BG_PANEL,
                    fg=tokens.FG_1,
                    font=self._rail_font(),
                    anchor="w",
                    padx=30,
                    pady=tokens.SP_1,
                    cursor="hand2",
                )
                item.pack(fill="x")
                tid = tool.id
                item.bind("<Button-1>", lambda _e, t=tid: self._activate_tool(t))  # type: ignore[misc]
                item.bind("<Enter>", lambda _e, w=item, t=tid: self._rail_hover(w, t))  # type: ignore[misc]
                item.bind("<Leave>", lambda _e, w=item, t=tid: self._rail_unhover(w, t))  # type: ignore[misc]
                self._rail_items[tool.id] = item

        # Divider
        div = tk.Frame(scroll_container, bg=tokens.BORDER_SUBTLE, height=1)
        div.pack(fill="x", padx=tokens.SP_3, pady=(tokens.SP_2, tokens.SP_1))

        # Static entries: Instructions, About, User
