    def _build_layout(self) -> None:
        """Construct the shell: app header | (rail | content) | status bar."""
        self.configure(style="TFrame")

        # Fix row height to a constant so Tk skips per-resize row-height
        # recomputation inside Treeview.  22 px matches the design's 4 px grid
        # (font ~13 px + 4 px padding above + 4 px below ≈ 21-22 px).
        # Without this Tk measures each row's font metrics on every Configure
        # event, which adds measurable work during window-drag.  (Round 14)
        _style = ttk.Style()
        _style.configure("Treeview", rowheight=22)

        # Round 19 — derived button style for "launcher" tools that present a
        # single oversized call-to-action (e.g. Refined PAL Search).  Tools
        # opt-in by setting ``primary_button_style = "Large.Accent.TButton"``;
        # the default remains ``Accent.TButton`` for every other tool so the
        # broad design contract from ADR-0014 stays intact.
        _style.configure(
            "Large.Accent.TButton",
            font=(self._fonts.sans_family, tokens.FS_14, "bold"),
            padding=(tokens.SP_4, tokens.SP_3),
        )

        # App header row (32 px) — SF chip + active-tool title
        self._app_header = self._build_app_header()
        self._app_header.pack(side="top", fill="x")

        # Outer vertical split: body + status bar
        self._body_frame = ttk.Frame(self)
        self._body_frame.pack(side="top", fill="both", expand=True)

        self._status_bar = self._build_status_bar()
        self._status_bar.pack(side="bottom", fill="x")

        # Horizontal split inside body: rail | content
        #
        # ADR-0014 deliberate exception (Round 19): the left rail's width was
        # previously locked at 220 px.  Users requested a draggable handle so
        # long tool labels and group headers can fit comfortably without
        # forcing every label to fit the original width.  We now hold the rail
        # and content panes inside a tk.PanedWindow so the user can drag the
        # divider; the 220 px default and a 180 px minsize preserve the design
        # intent for users who never touch the sash.
        self._body_paned = tk.PanedWindow(
            self._body_frame,
            orient=tk.HORIZONTAL,
            sashwidth=4,
            sashrelief=tk.FLAT,
            bg=tokens.BORDER_SUBTLE,
            borderwidth=0,
            showhandle=False,
            opaqueresize=False,
        )
        self._body_paned.pack(side="left", fill="both", expand=True)

        self._rail_frame = self._build_rail(parent=self._body_paned)
        self._content_outer = tk.Frame(self._body_paned, bg=tokens.BG_MUTED)

        # Pane 1 — rail.  The rail itself is built with width=220 and
        # pack_propagate(False), so the PanedWindow uses 220 as the initial
        # sash position.  minsize=180 stops the rail from collapsing past the
        # point at which tool labels truncate; stretch="never" keeps the rail
        # at its current width when the window resizes.
        self._body_paned.add(self._rail_frame, minsize=180, stretch="never")
        # Pane 2 — content.  stretch="always" makes the content area absorb
        # window-size changes; the rail width stays as the user left it.
        self._body_paned.add(self._content_outer, minsize=400, stretch="always")

        # Tool content frames (one per tool, stacked — only one visible at once)
        self._tool_frames: dict[str, tk.Frame] = {}
        self._tool_widgets: dict[str, _WidgetMap] = {}
        self._tool_banners: dict[str, Banner | None] = {}
        self._tool_progress: dict[str, ProgressBar | None] = {}
        self._tool_logviews: dict[str, LogView | None] = {}
        self._tool_tables: dict[str, Table | None] = {}
        self._tool_metric_frames: dict[str, tk.Frame | None] = {}
        self._tool_primary_btns: dict[str, ttk.Button | None] = {}
        self._tool_output_rows: dict[str, FileRow | None] = {}

        for tool in self._tools:
            frame, wmap = self._build_tool_frame(tool)
            # Do NOT pre-place all frames.  Only the active frame is placed at
            # any one time (see _activate_tool / _show_unlock_cta / _show_user).
            # Placing every frame with relwidth/relheight=1.0 causes Tk to fire
            # <Configure> on ALL of them — even the hidden (lowered) ones — on
            # every resize tick, multiplying layout work by the tool count.
            # (Fix 2 — Round 13)
            self._tool_frames[tool.id] = frame
            self._tool_widgets[tool.id] = wmap

        # Root-window resize debounce — one global handler as a safety net.
        # Fires only after the user has stopped dragging for ~100 ms (raised
        # from 50 ms in Round 14), which ensures any stray layout callbacks
        # settle without thrashing.
        self._root.bind("<Configure>", self._on_root_configure)

    def _build_app_header(self) -> tk.Frame:
        """32 px app-level header — navy SF chip with active tool's short mark + title text.

        Sits above the body; OS chrome sits above this. Matches the design handoff's
        ``desktop_multitool/tk-shell.jsx`` artboard (chip + title, no custom min/max/close).
        """
        header = tk.Frame(self, bg=tokens.BG_MUTED, height=32)
        header.pack_propagate(False)

        inner = tk.Frame(header, bg=tokens.BG_MUTED)
        inner.pack(fill="both", expand=True, padx=tokens.SP_3)

        # SF monogram chip: 16x16 navy square with the active tool's short mark.
        # Height is clamped via a frame wrapper so the label renders at exactly 16 px.
        chip_wrap = tk.Frame(inner, bg=tokens.BG_MUTED, width=16, height=16)
        chip_wrap.pack_propagate(False)
        chip_wrap.pack(side="left", pady=(tokens.SP_2, tokens.SP_2))

        self._header_chip = tk.Label(
            chip_wrap,
            text="",
            bg=tokens.BRAND_NAVY,
            fg=tokens.FG_INVERSE,
            font=tkfont.Font(family=self._fonts.sans_family, size=8, weight="bold"),
            anchor="center",
        )
        self._header_chip.pack(fill="both", expand=True)

        # Title text — updated on tool activation.
        self._header_title_var = tk.StringVar(value=APP_TITLE)
        tk.Label(
            inner,
            textvariable=self._header_title_var,
            bg=tokens.BG_MUTED,
            fg=tokens.FG_1,
            font=tkfont.Font(family=self._fonts.sans_family, size=tokens.FS_12),
            anchor="w",
        ).pack(side="left", padx=(tokens.SP_3, 0))

        # 1 px bottom border
        tk.Frame(header, bg=tokens.BORDER_SUBTLE, height=1).pack(side="bottom", fill="x")

        return header

