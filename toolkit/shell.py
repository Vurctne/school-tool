    def _run_tool(self, tool: BaseTool) -> None:
        tid = tool.id

        # Gather input values from widgets
        wmap = self._tool_widgets[tid]
        resolved: dict[str, Any] = {}

        for inp in tool.inputs:
            w = wmap.get(inp.key)
            if w is None:
                continue
            if isinstance(inp, FileInput) and isinstance(w, FileRow):
                p = w.get_path()
                if p:
                    resolved[inp.key] = p
                    self._input_cache[tid][inp.key] = p
            elif isinstance(inp, TextInput) and isinstance(w, TextField):
                resolved[inp.key] = w.get()
                self._input_cache[tid][inp.key] = w.get()
            elif isinstance(inp, NumberInput) and isinstance(w, NumberField):
                from decimal import Decimal

                raw = w.get()
                try:
                    resolved[inp.key] = Decimal(raw) if inp.decimals else int(raw)
                except Exception:
                    resolved[inp.key] = raw
                self._input_cache[tid][inp.key] = raw
            elif isinstance(inp, CurrencyInput) and isinstance(w, CurrencyField):
                from decimal import Decimal

                raw = w.get()
                try:
                    resolved[inp.key] = Decimal(raw)
                except Exception:
                    resolved[inp.key] = Decimal("0")
                self._input_cache[tid][inp.key] = raw
            elif isinstance(inp, DateInput) and isinstance(w, DateField):
                resolved[inp.key] = w.get_date()
                self._input_cache[tid][inp.key] = w.get_date()
            elif isinstance(inp, SecretInput) and isinstance(w, SecretField):
                resolved[inp.key] = w.get()

        # Add output path if present
        out_row = self._tool_output_rows.get(tid)
        if out_row and tool.output:
            out_path = out_row.get_path()
            if out_path:
                resolved[tool.output.key] = out_path

        # Disable inputs + show progress bar
        self._set_inputs_state(tid, "disabled")
        pb_frame = wmap.get("__pb_frame__")
        if pb_frame:
            pb_frame.pack(fill="x", padx=tokens.SP_4, pady=(tokens.SP_2, 0))
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(0, "Starting…")

        # Clear previous results
        self._clear_results(tid)

        self._set_status("Running…")
        logger.info("Starting tool: %s", tool.label)

        executor = self._executors[tid]

        def _progress_worker(pct: int, msg: str) -> None:
            self._root.after(0, lambda: self._on_progress(tid, pct, msg))

        def _worker() -> ToolResult:
            return tool.run(resolved, _progress_worker)

        future: Future[ToolResult] = executor.submit(_worker)
        future.add_done_callback(
            lambda f: self._root.after(0, lambda: self._on_tool_complete(tid, f))
        )

    def _on_progress(self, tid: str, pct: int, msg: str) -> None:
        if tid != self._active_id:
            return
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(pct, msg)
        self._set_status(msg)

    def _on_tool_complete(self, tid: str, future: Future[ToolResult]) -> None:
        # Re-enable inputs
        self._set_inputs_state(tid, "normal")

        exc = future.exception()
        if exc is not None:
            logger.exception("Tool %s raised an exception", tid, exc_info=exc)
            result = ToolResult(
                status="error",
                banner_level="danger",
                banner_text=f"Error: {exc}",
            )
        else:
            result = future.result()

        self._render_result(tid, result)

        # Hide progress bar
        wmap = self._tool_widgets[tid]
        pb_frame = wmap.get("__pb_frame__")
        if pb_frame:
            pb_frame.pack_forget()
        pb = self._tool_progress.get(tid)
        if pb:
            pb.set(100, result.banner_text[:80])

        self._set_status(result.banner_text[:100])
        logger.info("Tool %s completed with status %s", tid, result.status)

    def _render_result(self, tid: str, result: ToolResult) -> None:
        wmap = self._tool_widgets[tid]
        banner_frame = wmap.get("__banner_frame__")

        # Banner
        if banner_frame:
            for child in banner_frame.winfo_children():
                child.destroy()
            banner = Banner(banner_frame, level=result.banner_level, text=result.banner_text)
            banner.pack(fill="x")
            self._tool_banners[tid] = banner

        # Metrics
        metric_frame = self._tool_metric_frames.get(tid)
        if metric_frame:
            for child in metric_frame.winfo_children():
                child.destroy()
            if result.metrics:
                large_single = len(result.metrics) == 1
                metric_frame.pack(fill="x", padx=tokens.SP_4, pady=(tokens.SP_2, 0))
                row = tk.Frame(metric_frame, bg=tokens.BG_MUTED)
                row.pack(fill="x")
                for lbl, val, tone in result.metrics:
                    m = Metric(
                        row,
                        label=lbl,
                        value=val,
                        tone=tone or "neutral",
                        large=large_single,
                    )
                    m.pack(side="left", padx=(0, tokens.SP_3), expand=True)

        # Log lines
        lv = self._tool_logviews.get(tid)
        log_frame = wmap.get("__log_frame__")
        if lv and result.log_lines:
            lv.clear()
            for line in result.log_lines:
                lv.append(line)
            if log_frame:
                log_frame.pack(fill="both", expand=True, padx=tokens.SP_4, pady=(tokens.SP_2, 0))

        # Table
        table_frame = wmap.get("__table_frame__")
        if result.table_columns and result.table_rows is not None and table_frame:
            # Destroy previous table if any
            for child in table_frame.winfo_children():
                child.destroy()
            tbl = Table(table_frame, columns=result.table_columns)
            tbl.set_rows(result.table_rows)
            tbl.pack(fill="both", expand=True)
            table_frame.pack(fill="both", expand=True, padx=tokens.SP_4, pady=(tokens.SP_2, 0))
            self._tool_tables[tid] = tbl

    def _clear_results(self, tid: str) -> None:
        wmap = self._tool_widgets[tid]

        # Clear banner
        banner_frame = wmap.get("__banner_frame__")
        if banner_frame:
            for child in banner_frame.winfo_children():
                child.destroy()
        self._tool_banners[tid] = None

        # Clear metrics
        metric_frame = self._tool_metric_frames.get(tid)
        if metric_frame:
            for child in metric_frame.winfo_children():
                child.destroy()
            metric_frame.pack_forget()

        # Clear log
        lv = self._tool_logviews.get(tid)
        if lv:
            lv.clear()
        log_frame = wmap.get("__log_frame__")
        if log_frame:
            log_frame.pack_forget()

        # Clear table
        table_frame = wmap.get("__table_frame__")
        if table_frame:
            for child in table_frame.winfo_children():
                child.destroy()
            table_frame.pack_forget()
        self._tool_tables[tid] = None

    def _set_inputs_state(self, tid: str, state: str) -> None:
        """Enable or disable all input widgets and the primary button for a tool."""
        wmap = self._tool_widgets[tid]
        tool = self._tool_map[tid]

        for inp in tool.inputs:
            w = wmap.get(inp.key)
            if w is None:
                continue
            if isinstance(w, FileRow):
                w.set_state(state)
            else:
                try:
                    w.configure(state=state)
                except tk.TclError:
                    pass

        out_row = self._tool_output_rows.get(tid)
        if out_row:
            out_row.set_state(state)

        primary = self._tool_primary_btns.get(tid)
        if primary:
            primary.configure(state=state)

    # -----------------------------------------------------------------------
    # Global exception handler
    # -----------------------------------------------------------------------

    def _handle_tk_exception(
        self,
        exc_type: type[BaseException],
        exc_value: BaseException,
        exc_tb: Any,
    ) -> None:
        tb_str = "".join(traceback.format_exception(exc_type, exc_value, exc_tb))
        logger.error("Unhandled exception in Tk callback:\n%s", tb_str)

        msg = str(exc_value)
        log_hint = f"\nSee log at: {self._log_path}" if self._log_path else ""

        # Show a danger banner in the active tool frame
        if self._active_id and self._active_id in self._tool_widgets:
            wmap = self._tool_widgets[self._active_id]
            banner_frame = wmap.get("__banner_frame__")
            if banner_frame:
                for child in banner_frame.winfo_children():
                    child.destroy()
                banner = Banner(
                    banner_frame,
                    level="danger",
                    text=f"An unexpected error occurred: {msg}{log_hint}",
                )
                banner.pack(fill="x")

